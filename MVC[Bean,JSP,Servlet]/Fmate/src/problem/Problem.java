package problem;

public class Problem {
	private int prID;
	private String prAuth;
	private String prTitle;
	private String userID;
	private String prDate;
	private String prContent;
	private int prAvailable;
	
	public int getPrID() {
		return prID;
	}
	public void setPrID(int prID) {
		this.prID = prID;
	}
	
	public String getPrAuth() {
		return prAuth;
	}
	public void setPrAuth(String prAuth) {
		this.prAuth = prAuth;
	}
	
	public String getPrTitle() {
		return prTitle;
	}
	public void setPrTitle(String prTitle) {
		this.prTitle = prTitle;
	}
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	
	public String getPrDate() {
		return prDate;
	}
	public void setPrDate(String prDate) {
		this.prDate = prDate;
	}
	public String getPrContent() {
		return prContent;
	}
	public void setPrContent(String prContent) {
		this.prContent = prContent;
	}
	public int getPrAvailable() {
		return prAvailable;
	}
	public void setPrAvailable(int prAvailable) {
		this.prAvailable = prAvailable;
	}
	
}
